public class Fahrenheit_to_celcius {

	public static void main(String[] args) {
		int fahrenheit1=100;
		int celcius1;
		System.out.println("Enter fahrenheit:"+fahrenheit1);
		celcius1=(fahrenheit1-32)*5/9;
		System.out.println("celcius1: "+celcius1);
	}

}
